package com.example.a21111064571_nguyenvanchuong_lab05;

public class PerSon {
    private int imgPerSon;
    private String namePerSon;

    public PerSon(int imgPerSon, String namePerSon) {
        this.imgPerSon = imgPerSon;
        this.namePerSon = namePerSon;
    }

    public int getImgPerSon() {
        return imgPerSon;
    }

    public void setImgPerSon(int imgPerSon) {
        this.imgPerSon = imgPerSon;
    }

    public String getNamePerSon() {
        return namePerSon;
    }

    public void setNamePerSon(String namePerSon) {
        this.namePerSon = namePerSon;
    }
}
